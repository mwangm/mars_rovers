package practice.mars.area;

public class Plateau {
    private int borderX;
    private int borderY;

    public Plateau(int borderX, int borderY) {
        this.borderX = borderX;
        this.borderY = borderY;
    }

    public int getBorderX() {
        return borderX;
    }

    public int getBorderY() {
        return borderY;
    }
}
